    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String buscar = txtBuscar.getText();
        EmpresaDAO dao = new EmpresaDAO();
        table.setNumRows(0);
        for (EmpresaBEAN empresa : dao.getNome(buscar)) {
            Object[] dados = {empresa.getCodigo(), empresa.getNome(), empresa.getCnpj(), empresa.getTelefone(), empresa.getEstado(), empresa.getEndereco(), empresa.getBairro(), empresa.getLogradouro()};
            table.addRow(dados);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

